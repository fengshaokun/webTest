public class Sycl {

    private  String gg;
    private  String sb;
    private  Double cl;

    @Override
    public String toString() {
        return "Sycl{" +
                "gg='" + gg + '\'' +
                ", sb='" + sb + '\'' +
                ", cl='" + cl + '\'' +
                '}';
    }

    public String getGg() {
        return gg;
    }

    public void setGg(String gg) {
        this.gg = gg;
    }

    public String getSb() {
        return sb;
    }

    public void setSb(String sb) {
        this.sb = sb;
    }

    public Double getCl() {
        return cl;
    }

    public void setCl(Double cl) {
        this.cl = cl;
    }
}
